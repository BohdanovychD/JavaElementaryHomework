package lesson_11.entity;

public class Client {

    private String clientIdentify;
    private String secondName;
    private String clientAccIdentify;
    private double sum;

    public String getClientIdentify() {
        return clientIdentify;
    }

    public void setClientIdentify(String clientIdentify) {
        this.clientIdentify = clientIdentify;
    }

    public String getSecondName() {
        return secondName;
    }

    public void setSecondName(String secondName) {
        this.secondName = secondName;
    }

    public String getClientAccIdentify() {
        return clientAccIdentify;
    }

    public void setClientAccIdentify(String clientAcc) {
        this.clientAccIdentify = clientAcc;
    }

    public double getSum() {
        return sum;
    }

    public void setSum(double sum) {
        this.sum = sum;
    }

}
